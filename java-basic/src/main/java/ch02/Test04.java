// 정수 값을 메모리에 저장하는 원리
package ch02;

public class Test04 {
  public static void main(String[] args) {
    // 5 - 5 의 결과를 출력하라.
    System.out.println(5 - 5);
    
    // 5 + (-5) 의 결과를 출력하라.
    System.out.println(5 +(-5));
  }
}

/*
# 정수를 메모리에 저장하는 방법
* - 전기적 신호로 저장한다. => 각 비트의 전기가 있고 없고의 상태로 표현한다.
* - 이론이나 프로그램 코드에서는 비트에 전기가 있는 상태를 1로 표현하고 없는 상태를 0으로 표현한다.
* - 즉 값을 저장하려면 0과 1로 표현이 가능해야 한다.
* - 2진수로 표현할 수 있어야 한다.
*/
